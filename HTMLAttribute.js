// =========================================== HTML Attribute ============================================== //
// ChatGPT Se liya hua Syntax ---> HTML attributes elements ko additional information dete hain ya unka behavior control karte hain. Attributes hamesha opening tag ke andar hote hain aur name/value pair mein aate hain, jaise name="value".


// =============================================== Id Attribute ================================================== //
// ChatGPT Se liya hua Syntax ---> 1. 1. id Attribute
// id attribute kisi bhi HTML element ko ek unique pehchaan (identifier) deta hai, jise CSS ke saath style karne ya JavaScript ke saath manipulate karne ke liye use kiya ja sakta hai.

{/* <p id="intro">Yeh ek introductory paragraph hai.</p> */}


// ============================================== Class Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 2. class Attribute
// class attribute ek ya ek se zyada class names ko specify karta hai jo element par apply hote hain. Class ek unique attribute nahi hota, ek se zyada elements me ek hi class use ki ja sakti hai.

{/* <p class="highlight">Yeh paragraph highlight kiya gaya hai.</p>
<p class="highlight">Ek aur highlighted paragraph.</p> */}


// ============================================== style Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 3. style Attribute
// style attribute ke madhyam se inline CSS styling apply ki ja sakti hai. Yadi ho sake to external ya internal CSS style use karein, lekin style attribute jaldi aur specific changes ke liye upyogi hota hai.

{/* <p style="color: blue; font-size: 20px;">Yeh text blue aur bada hai.</p> */}


// ============================================== Href Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 4. href Attribute (hypertext reference).
// href attribute ko <a> (anchor) tag ke saath use kiya jaata hai, jo link ka URL specify karta hai.

{/* <a href="https://www.example.com">Example.com ko visit karein</a> */}


// ============================================== Src Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 5. src Attribute "source".
// src attribute kisi image, video, audio, ya script file ka path specify karta hai. Yeh mostly <img>, <script>, <audio>, aur <video> tags ke saath use hota hai.
{/* <img src="image.jpg" alt="Sundar drishya"> */}


// ============================================== Alt Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 6. alt Attribute
// alt attribute image ke display na hone par alternative text provide karta hai. Yeh accessibility aur SEO ke liye important hai.

{/* <img src="image.jpg" alt="Sundar drishya"> */}


// ============================================== Title Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 7. title Attribute
// title attribute element ke baare me additional information provide karta hai, jo aksar tooltip ke roop me dikhai deti hai jab mouse us element par hover karta hai.

{/* <p title="Yeh additional information hai">Is paragraph par hover karein title attribute dekhne ke liye.</p> */}



// ============================================= data-* Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 8.  data-* Attributes
// data-* attributes custom data attributes hote hain jo standard HTML elements me extra information store karne ke liye use hote hain. Attribute name data- se shuru hota hai aur JavaScript ke madhyam se value ko access kiya ja sakta hai.

{/* <div data-user-id="12345">User data ek custom attribute me stored hai.</div> */}




// ============================================= target Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 9.  target Attribute
// target attribute specify karta hai ki linked document kahan khulega. Ye aksar <a> tags ke saath use hota hai.


{/* <a href="https://www.example.com" target="_blank">Naye tab me kholne ke liye click karein</a> */}



// =========================================== placeholder Attribute ============================================= //
// ChatGPT Se liya hua Syntax ---> 10. placeholder Attribute
// placeholder attribute input field me ek short hint provide karta hai jo expected value ko describe karta hai.

{/* <input type="text" placeholder="Apna naam enter karein"> */}




