// ChatGPT Se liya hua Syntax ---> HTML semantic tags ka use web pages ko behtar structure aur accessibility dene ke liye hota hai. Ye tags page ke content ke purpose ko human aur machine dono ke liye clearly describe karte hain. Yahaan kuch common semantic tags aur unke meaning hain.


// 1. <header>
// <header> tag page ke introductory content ya navigation links ke set ko represent karta hai. Isme usually logo, site name, navigation menu, aur dusre introductory content hote hain.

//2. <nav>
//<nav> tag navigation links ke set ko define karne ke liye use hota hai. Ye links other pages ya same page ke parts ko link karte hain.


// 3. <main>
//<main> tag page ke main content ko represent karta hai. Ye unique hona chahiye aur sidebars, headers, aur footers ko exclude karta hai.



// 4. <section>
//<section> tag ek standalone content section ko represent karta hai, jo heading ke saath hota hai. Ye usually related content ko group karne ke liye use hota hai.



// 5. <article>
//<article> tag self-contained content ko represent karta hai jo independently distribute ya reuse kiya ja sakta hai. Examples mein blog posts, news articles, aur user comments shamil hain.




// 6. <aside>
// <aside> tag aise content ke liye hota hai jo main content se thoda related hota hai. Ye usually sidebars, pull quotes, ya advertising ke liye use hota hai.

// 7. <footer>
// <footer> tag section ya page ke footer ko represent karta hai. Isme usually author information, copyright information, ya related documents ke links hote hain.



// 8. <figure> aur <figcaption>
// <figure> tag wo content represent karta hai jo main content ko illustrate ya support karta hai, jaise images, diagrams, ya code snippets. <figcaption> tag <figure> ke andar caption ya description provide karta hai.


