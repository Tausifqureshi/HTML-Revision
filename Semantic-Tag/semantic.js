// 2. Better SEO: Search engines semantic tags ke through page ke structure aur context ko better samajh sakte hain, jo ranking aur indexing ko behtar banata hai.

// 3. Enhanced Readability: Semantic tags HTML code ko zyada readable aur maintainable banate hain, kyunki har section ka purpose clear hota hai.

