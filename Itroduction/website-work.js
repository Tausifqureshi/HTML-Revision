//  ChatGPT Se liya hua Syntax ---> Website kaam karne ka basic process kuch is tarah se hota hai:

// 1. User Request: Jab aap apne browser me kisi website ko access karte hain, to aapka browser server ko ek request bhejta hai.

// 2. Domain Name System (DNS): DNS aapke domain name ko IP address me convert karta hai. Ye IP address website ke server ka hota hai.

// 3. Server Processing: Server jo request aayi hai, usko process karta hai. Server pe website ka code (HTML, CSS, JavaScript) aur data stored hota hai.

// 4. Response: Server processed information ko browser ko send karta hai. Yeh usually HTML page hota hai.

// 5. Rendering: Browser HTML, CSS, aur JavaScript ko interpret karta hai aur webpage ko render karta hai jo aap dekh sakte hain.

// 6. Interactivity: Agar webpage me JavaScript hai, to browser usse execute karta hai aur webpage ko interactive banata hai.