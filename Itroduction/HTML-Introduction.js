// ================================================ HTML History ================================================= //
//  ChatGPT Se liya hua Syntax --->  HTML History.
// 1. Early 1990s: Invention
// 1991: Tim Berners-Lee ne HTML ko develop kiya tha. Unka idea ek universal hypertext system banane ka tha jo information ko easily share aur access kar sake. Unhone HTML ko first time CERN (European Organization for Nuclear Research) mein introduce kiya.

// 2. HTML 1.0 (1991)
// 1991: HTML ka pehla version release hua. Yeh version basic markup tags aur links ko support karta tha. Yeh bahut hi simple aur limited functionality wala tha.

// 3. HTML 2.0 (1995)
// 1995: HTML 2.0 ko introduce kiya gaya, jo ki W3C (World Wide Web Consortium) ke dwara standardized version tha. Is version ne forms, tables, aur other features ko add kiya jo web pages ko aur interactive aur structured banaate hain.

// 4. HTML 3.2 (1997)
// 1997: HTML 3.2 release hua jisme ne naye elements aur attributes ko add kiya gaya, jaise ki style sheets (CSS) aur scripting (JavaScript). Yeh version web pages ko zyada flexible aur visually appealing banata hai.

// 5. HTML 4.0 (1999)
// 1999: HTML 4.0 ko release kiya gaya jisme separation of content and presentation ko emphasize kiya gaya. Isme CSS ka use badh gaya aur web pages ko layout aur styling ke liye zyada control diya gaya.

// 6. HTML 4.01 (2000)
// 2000: HTML 4.01, HTML 4.0 ka updated version tha, jo kuch minor improvements aur bug fixes ko include karta tha.

// 7. HTML5 (2014)
// 2014: HTML5 ko officially release kiya gaya. Yeh version modern web applications ke liye design kiya gaya tha aur multimedia (audio, video), graphics (Canvas), aur new semantic elements (like <header>, <footer>, <article>, etc.) ko include karta tha. Is version ne JavaScript APIs aur local storage ko bhi support kiya, jo rich web applications aur better user experiences provide karta hai.

