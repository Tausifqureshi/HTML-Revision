// ============================================== Audio Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> Audio Tag Attribute.

// 1. src: Yeh attribute audio file ka URL specify karta hai. Isme aap file ka path ya URL provide karte hain.

// 2. controls: Yeh optional attribute audio player ke controls (play, pause, volume) ko display karta hai. Agar aap yeh attribute use nahi karte, to player controls nahi dikhaye jayenge.

// 3. autoplay: Is attribute se audio file automatically play hoti hai jab page load hota hai. Example: <audio src="audio-file.mp3" autoplay></audio>

// 4. loop: Is attribute se audio file repeat hoti hai jab file end hoti hai. Example: <audio src="audio-file.mp3" loop></audio>

// 5. muted: Is attribute se audio file mute ho jati hai. Example: <audio src="audio-file.mp3" muted></audio>

// 6. preload: Is attribute se browser ko decide karne ki permission milti hai ki audio file ko preload karna hai ya nahi. Iske possible values hain.z
// none: Audio file ko preload nahi kiya jayega.
// metadata: Only metadata preload kiya jayega.
// auto: Puri audio file preload ki jayegi. Example: <audio src="audio-file.mp3" preload="auto"></audio>