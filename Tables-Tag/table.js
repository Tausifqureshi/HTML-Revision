// ========================================= Table Ka Basic Structure ============================================ //
// ChatGPT Se liya hua Syntax ---> HTML Table Ka Basic Structure.
// 1. <table>: Ye poore table ka container element hai.

// 2. <tr>: Ye ek table row ko represent karta hai. <table> ke andar <tr> ka use rows define karne ke liye hota hai.

// 3. <td>: Ye table cell (data) ko represent karta hai. Har <tr> me ek ya zyada <td> elements hote hain.

// 4. <th>: Ye table header cell ko represent karta hai. Ye headings ke liye use hota hai aur by default bold aur centered hota hai. Ye bhi <tr> elements ke andar hota hai.

// 5. <caption>: Table ke liye ek title ya description provide karta hai. Ye <table> tag ke baad aata hai.

// 6. <thead>: Table ke header content ko group karta hai. Ye header rows define karne me help karta hai.

// 7. <tbody>: Table ke body content ko group karta hai. Ye sab data rows ko contain karta hai.

// 8.  <tfoot>: Table ke footer content ko group karta hai. Ye summary ya footer rows ke liye use hota hai.




