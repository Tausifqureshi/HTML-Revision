// ================================================ Title Tag ==================================================== //
// ChatGPT Se liya hua Syntax ---> 1. Title Tag.

// Usage: <title> tag ko webpage ke title ko specify karne ke liye use kiya jata hai.

// SEO Importance: Yeh tag search engine results pages (SERPs) pe dikhayi jati hai, aur yeh page ke content ke baare me clear information dena chahiye.

// Best Practices: Title ko short aur descriptive rakhein, 50-60 characters ke andar. Primary keywords ko include karna chahiye.

// <title>Best Organic Coffee Beans | Coffee Shop</title>

// ============================================= Meta Description ================================================ //
// ChatGPT Se liya hua Syntax ---> 2. Meta Description.
// Usage: <meta name="description" content="..."> tag webpage ka short summary provide karta hai jo search results me dikhai jati hai.

// SEO Importance: Yeh tag users ko webpage ke content ke baare me bataata hai aur click-through rates ko impact kar sakta hai.
// Best Practices: Meta description ko 150-160 characters ke andar rakhein aur primary keywords ko include karein.

//<meta name="description" content="Discover the finest organic coffee beans at our coffee shop. Freshly roasted and brewed to perfection.">

// =============================================== Heading Tags ================================================== //
// ChatGPT Se liya hua Syntax ---> 3. Heading Tags
// Usage: <h1>, <h2>, <h3>, etc. tags content ko organize karne ke liye use kiye jate hain.
// SEO Importance: Heading tags content ke structure ko define karte hain aur search engines ko content ke importance ke baare me bataate hain.

// Best Practices: Page ka main heading <h1> tag me hona chahiye aur subheadings <h2>, <h3>, etc. me. Keywords ko naturally include karein.

//<h1>Organic Coffee Beans for Every Taste</h1>
//<h2>Our Best Sellers</h2>

// =========================================== Alt Text for Images =============================================== //
// ChatGPT Se liya hua Syntax ---> 4.  Alt Text for Images.
// Description: Alt text images ke description ko provide karta hai jo search engines aur screen readers ke liye helpful hota hai.

// Best Practice: Alt text ko descriptive aur relevant banayein. Yeh 125 characters ke around hona chahiye.

//<img src="pizza.jpg" alt="Delicious pepperoni pizza with extra cheese">

// =============================================== URL Structure ================================================= //
// ChatGPT Se liya hua Syntax ---> 5.  URL Structure
// Description: Clean aur descriptive URLs search engines aur users dono ke liye helpful hote hain.
// Best Practice: URLs ko short aur descriptive rakhein. Keywords ko URL me include karein.

//<a href="https://example.com/best-pizza-new-york">Best Pizza in New York</a>

// =============================================== Anchor Text =================================================== //
// ChatGPT Se liya hua Syntax ---> 6. Anchor Text.
// Description: Anchor text hyperlinks me text hota hai. Yeh search engines ko batata hai ki link ki destination page kis baare me hai.
// Best Practice: Anchor text ko relevant aur descriptive banayein.
// html

// <a href="https://example.com/pizza-offers">Check out our special pizza offers</a>

// ============================================ Mobile-Friendliness ============================================== //
// ChatGPT Se liya hua Syntax ---> 7. Mobile-Friendliness
// Description: Mobile-friendly design search engines me better ranking ke liye zaroori hai.
// Best Practice: Responsive web design use karein aur viewport meta tag include karein.

// <meta name="viewport" content="width=device-width, initial-scale=1.0">

// =============================================  Internal Linking =============================================== //
// ChatGPT Se liya hua Syntax ---> 8.  Internal Linking
// Description: Internal links website ke pages ke beech navigation ko enhance karte hain.
// Best Practice: Relevant aur contextual links use karein.

// <a href="https://example.com/menu">View our menu</a>

