// ============================================= Input-Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> HTML input elements ke attributes aur unka vyavhaar, appearance, aur data ke prakar ko kaise accept karte hain, yeh sab specify karte hain.


// ============================================= Name Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 1. Name Attribute (name)

// name attribute input element ka naam specify karta hai, jo form submit karte waqt server ko bheja jata hai. Yeh form data ko identify karne ke liye important hota hai.


// ============================================= Value Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 2. Value Attribute (value)

// value attribute input element ka initial value define karta hai. Buttons (submit, reset, etc.) ke liye, yeh button ka label set karta hai.


// ========================================== Placeholder Attribute ============================================= //
// ChatGPT Se liya hua Syntax ---> 3. Placeholder Attribute (placeholder)

// placeholder attribute ek short hint provide karta hai jo input field ke expected value ko describe karta hai. Yeh hint tab display hota hai jab field khali hota hai.



// =========================================== Required  Attribute ============================================== //
// ChatGPT Se liya hua Syntax ---> 4. Required Attribute (required)

// required ek boolean attribute hai jo specify karta hai ki form submit karne se pehle user ko ek value fill karni chahiye.


// =========================================== Readonly  Attribute ============================================== //
// ChatGPT Se liya hua Syntax ---> 5. Readonly Attribute (readonly)

// readonly attribute input field ko non-editable banata hai, lekin yeh form ke saath submit hota hai. User value ko dekh sakta hai, lekin usko change nahi kar sakta.


// ============================================ Disabled Attribute =============================================== //
// ChatGPT Se liya hua Syntax ---> 6. Disabled Attribute (disabled)

// disabled attribute input field ko unusable aur uneditable banata hai. Yeh form data ke saath submit nahi hota.


// ============================================ Maxlength Attribute ============================================== //
// ChatGPT Se liya hua Syntax ---> 7. Maxlength Attribute (maxlength)

// maxlength attribute specify karta hai ki user input field mein kitne maximum characters dal sakta hai.


// =========================================== Minlength Attribute ============================================== //
// ChatGPT Se liya hua Syntax ---> 8. Minlength Attribute (minlength)

// minlength attribute specify karta hai ki user input field mein kitne minimum characters dalna chahiye.


// ============================================ Pattern Attribute =============================================== //
// ChatGPT Se liya hua Syntax ---> 9. Pattern Attribute (pattern)

// pattern attribute ek regular expression specify karta hai jo input ke value ko match karna chahiye taaki form submit ho sake. Yeh validation ke liye use hota hai.


// ============================================= Step Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 10. Step Attribute (step)

// step attribute interval specify karta hai jo number, range, date, datetime-local, month, time, aur week inputs ke liye valid values ke beech hota hai.


// ========================================== Min and Max Attribute ============================================= //
// ChatGPT Se liya hua Syntax ---> 11. Min and Max Attributes (min, max)

// min aur max attributes number, range, date, datetime-local, month, week, aur time inputs ke liye minimum aur maximum values specify karte hain.


// ========================================== Autocomplete Attribute ============================================= //
// ChatGPT Se liya hua Syntax ---> 12. Autocomplete Attribute (autocomplete)

// autocomplete attribute specify karta hai ki input field mein autocomplete enabled hona chahiye ya nahi. Isko "on" ya "off" set kiya ja sakta hai, aur yeh specific values jaise name, email, username, etc. ke saath bhi use kiya ja sakta hai jo specific data types ke liye helpful hote hain.



// ============================================ AutofocusAttribute =============================================== //
// ChatGPT Se liya hua Syntax ---> 13. Autofocus Attribute (autofocus)

// autofocus ek boolean attribute hai jo specify karta hai ki page load hote hi input field ko automatic focus mil jana chahiye.

// ============================================ Multiple Attribute =============================================== //
// ChatGPT Se liya hua Syntax ---> 14. Multiple Attribute (multiple)

// multiple attribute, jab present hota hai, user ko input field mein ek se adhik value dalne ki anumati deta hai. Yeh commonly file inputs ke saath use hota hai taaki multiple file uploads allow ho sake.


// ============================================= Accept Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 15. Accept Attribute (accept)
// accept attribute specify karta hai ki server kaunse file types accept karta hai (jo file upload ke through submit ho sakte hain). Yeh commonly file input type ke saath use hota hai.


// ============================================= Size Attribute ================================================ //
// ChatGPT Se liya hua Syntax ---> 16. Size Attribute (size)
// size attribute ek input element ki visible width, characters mein specify karta hai. Yeh mostly text, search, URL, aur email inputs ke saath use hota hai.


// ============================================== List Attribute ================================================= //
// ChatGPT Se liya hua Syntax ---> 17. List Attribute (list)
// list attribute ek <datalist> element ko refer karta hai jo <option> elements contain karta hai. Yeh user ko predefined options provide karta hai jinme se wo chun sakte hain.


